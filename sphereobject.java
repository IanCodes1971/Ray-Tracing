public class sphereobject 
{
    public int x, y, z;
    public int radius;
    public int r, g, b;

    public sphereobject(int x, int y, int z, int radius, int r, int g, int b) 
	{
        this.x = x;
        this.y = y;
        this.z = z;
        this.radius = radius;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
